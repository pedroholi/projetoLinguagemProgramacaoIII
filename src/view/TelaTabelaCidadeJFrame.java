    private void JBexcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBexcluirActionPerformed

        int opcao = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir a cidade", "Mensagem do Sistema", JOptionPane.INFORMATION_MESSAGE);

        if (opcao == 0) {
            controller.deletar(this.criarObjeto());
            limparCampus();
            habilitarCampus(3);
            tbCidades.removeRow(jTCidades.getSelectedRow());
        }

    }//GEN-LAST:event_JBexcluirActionPerformed

