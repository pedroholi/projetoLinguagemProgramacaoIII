    public void limparCampus() {
        JTFpesquisar.setText("Digite o código ou nome da cidade");
    }

    public void habilitarCampus(int btn) {
        switch (btn) {
            case 1://Botão Pesquisar
                JBnovo.setEnabled(false);
                JBalterar.setEnabled(true);
                JBexcluir.setEnabled(true);
                JBcancelar.setEnabled(true);
                break;
            case 2://Botão Novo
                TelaCadCidadeJFrame cadCidade = new TelaCadCidadeJFrame();
                cadCidade.setVisible(true);
                break;
            case 3://Botões Salvar, Alterar, Excluir e Cancelar
                JBpesquisar.setEnabled(true);
                JTFpesquisar.setEnabled(true);
                JBnovo.setEnabled(true);
                JBalterar.setEnabled(false);
                JBexcluir.setEnabled(false);
                JBcancelar.setEnabled(false);
                break;
            default:
                break;
        }
    }

    public Cidade criarObjeto(){
        cid = new Cidade();
        
        cid.setNome(String.valueOf(jTCidades.getValueAt(jTCidades.getSelectedRow(), 1)));
        cid.setUf(String.valueOf(jTCidades.getValueAt(jTCidades.getSelectedRow(), 2)));
        cid.setPais(String.valueOf(jTCidades.getValueAt(jTCidades.getSelectedRow(), 3)));
        cid.setCodigo(Integer.parseInt(jTCidades.getValueAt(jTCidades.getSelectedRow(), 0).toString()));
        
        return cid;
    }
    
    public void pesquisar(){
        cid = controller.pesquisar(JTFpesquisar.getText());
        
        if (cid.getCodigo() == 0) {
            this.limparCampus();
            this.habilitarCampus(3);
            JOptionPane.showMessageDialog(null, "Cidade não encontrada", "Mensagem do Sistema", JOptionPane.INFORMATION_MESSAGE);
        } else {
            tbCidades.addRow(new Object[]{cid.getCodigo(), cid.getNome(), cid.getUf(), cid.getPais()});
           
            habilitarCampus(1);
        }
    }
    
    public void listarCidades(){
        
        ArrayList<Cidade> cidades = controller.selecionarCidades();
        
        for (Cidade cid : cidades) {
            tbCidades.addRow(new Object[]{cid.getCodigo(), cid.getNome(), cid.getUf(), cid.getPais()});
        }
    }
    
