    private void JBpesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBpesquisarActionPerformed
        String pesquisa = JTFpesquisa.getText();
        Usuario uso;
        UsuarioDAO daoUso = new UsuarioDAO();
        uso = daoUso.pesquisar(pesquisa);
        if(uso.getCodigo() == 0){
            JOptionPane.showMessageDialog(null, "Usuario nÃ£o encontrada", "Cadastro Cidade", 0);
        }else{
            JTFcodigo.setText(Integer.toString(uso.getCodigo()));
            JTFnome.setText(uso.getNome());
            JTFemail.setText(uso.getEmail());
            JTFsenha.setText(uso.getSenha());
            JBnovo.setEnabled(false);
            JBalterar.setEnabled(true);
            JBexcluir.setEnabled(true);
            JTFnome.setEnabled(true);
            JTFemail.setEnabled(true);
            JTFsenha.setEnabled(true);
        }
    }//GEN-LAST:event_JBpesquisarActionPerformed

    private void JBalterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBalterarActionPerformed
        UsuarioDAO daoUso = new UsuarioDAO();
        String nome = JTFnome.getText();
        String email = JTFemail.getText();
        String senha = JTFsenha.getText();
        int codigo = Integer.parseInt(JTFcodigo.getText());
        
        Usuario usuario = new Usuario();
        usuario.setNome(nome);
        usuario.setEmail(email);
        usuario.setSenha(senha);
        usuario.setCodigo(codigo);
        
        int opcao = JOptionPane.showConfirmDialog(null, "Deseja realmente atualizar os dados do usuario","Mensagem do Sistema",0);
        if(opcao == 0){
            daoUso.alterar(usuario);
        
        JTFnome.setEnabled(false);
        JTFemail.setEnabled(false);
        JTFsenha.setEnabled(false);
        JBnovo.setEnabled(true);
        JBexcluir.setEnabled(false);
        JBcancelar.setEnabled(false);
        JBalterar.setEnabled(false);
        JBsalvar.setEnabled(false);
        JTFcodigo.setText("");
        JTFnome.setText("");
        JTFemail.setText("");
        JTFsenha.setText("");
        }
    }//GEN-LAST:event_JBalterarActionPerformed

    private void JBexcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBexcluirActionPerformed
        int codigo = Integer.parseInt(JTFcodigo.getText());
        Usuario uso = new Usuario();
        uso.setCodigo(codigo);
        UsuarioDAO daoUso = new UsuarioDAO();
        int opcao = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir o usuario","Mensagem do Sistema",0);
        if(opcao == 0){
            daoUso.deletar(uso);
        }
        JTFnome.setEnabled(false);
        JTFemail.setEnabled(false);
        JTFsenha.setEnabled(false);
        JBnovo.setEnabled(true);
        JBexcluir.setEnabled(false);
        JBcancelar.setEnabled(false);
        JBalterar.setEnabled(false);
        JBsalvar.setEnabled(false);
        JTFcodigo.setText("");
        JTFnome.setText("");
        JTFemail.setText("");
        JTFsenha.setText("");
    }//GEN-LAST:event_JBexcluirActionPerformed

