    public void limparCampus() {
        JTFcodigo.setText("");
        JTFcidade.setText("");
        JTFpais.setText("");
        JTFpesquisar.setText("Digite o código ou nome da cidade");
        JCBestado.setSelectedIndex(0);
    }

    public void habilitarCampus(int btn) {
        switch (btn) {
            case 1://Botão Pesquisar
                JBnovo.setEnabled(false);
                JBalterar.setEnabled(true);
                JBexcluir.setEnabled(true);
                JBcancelar.setEnabled(true);
                JBsalvar.setEnabled(false);
                JTFcidade.setEnabled(true);
                JTFpais.setEnabled(true);
                JCBestado.setEnabled(true);
                jBanterior.setEnabled(true);
                jBprimeiro.setEnabled(true);
                jBproximo.setEnabled(true);
                jBultimo.setEnabled(true);
                break;
            case 2://Botão Novo
                JBsalvar.setEnabled(true);
                JBcancelar.setEnabled(true);
                JBpesquisar.setEnabled(false);
                JBalterar.setEnabled(false);
                JBnovo.setEnabled(false);
                JBexcluir.setEnabled(false);
                JTFpesquisar.setEnabled(false);
                JTFcidade.setEnabled(true);
                JTFpais.setEnabled(true);
                JCBestado.setEnabled(true);
                break;
            case 3://Botões Salvar, Alterar, Excluir e Cancelar
                JBpesquisar.setEnabled(true);
                JTFpesquisar.setEnabled(true);
                JTFcidade.setEnabled(false);
                JCBestado.setEnabled(false);
                JTFpais.setEnabled(false);
                JBnovo.setEnabled(true);
                JBsalvar.setEnabled(false);
                JBalterar.setEnabled(false);
                JBexcluir.setEnabled(false);
                JBcancelar.setEnabled(false);
                jBanterior.setEnabled(false);
                jBprimeiro.setEnabled(false);
                jBproximo.setEnabled(false);
                jBultimo.setEnabled(false);
                break;
            default:
                break;
        }
    }

    public Cidade criarObjeto() {
        cid = new Cidade();

        cid.setNome(JTFcidade.getText());
        cid.setUf(JCBestado.getSelectedItem().toString());
        cid.setPais(JTFpais.getText());
        if (JTFcodigo.getText().equals("")) {
            cid.setCodigo(0);
        } else {
            cid.setCodigo(Integer.parseInt(JTFcodigo.getText()));
        }

        return cid;
    }

    public void pesquisar() {
        cid = controller.pesquisar(JTFpesquisar.getText());

        if (cid.getCodigo() == 0) {
            this.limparCampus();
            this.habilitarCampus(3);
            JOptionPane.showMessageDialog(null, "Cidade não encontrada", "Mensagem do Sistema", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JTFcodigo.setText(Integer.toString(cid.getCodigo()));
            JTFcidade.setText(cid.getNome());
            JTFpais.setText(cid.getPais());
            JCBestado.setSelectedItem(cid.getUf());
            habilitarCampus(1);
        }
    }

    public void pesquisar(int codigo) {
        cid = controller.pesquisar(codigo);

        if (cid.getCodigo() == 0) {
            this.limparCampus();
            this.habilitarCampus(3);
            JOptionPane.showMessageDialog(null, "Cidade não encontrada", "Mensagem do Sistema", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JTFcodigo.setText(Integer.toString(cid.getCodigo()));
            JTFcidade.setText(cid.getNome());
            JTFpais.setText(cid.getPais());
            JCBestado.setSelectedItem(cid.getUf());
            habilitarCampus(1);
        }
    }

    public boolean verificarCampus() {

        boolean preenchido = true;

        if (JTFcidade.getText().isEmpty()) {
            JTFcidade.setText("O campo nome não pode ficar vazio");
            JTFcidade.setBackground(Color.YELLOW);
            preenchido = false;
        }
        if (JTFpais.getText().isEmpty()) {
            JTFpais.setText("O campo pais não pode ficar vazio");
            JTFpais.setBackground(Color.YELLOW);
            preenchido = false;
        }

        return preenchido;
    }
    
