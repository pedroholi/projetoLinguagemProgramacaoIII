    private void JBexcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBexcluirActionPerformed

        int opcao = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir a cidade", "Mensagem do Sistema", JOptionPane.INFORMATION_MESSAGE);

        if (opcao == 0) {
            controller.deletar(this.criarObjeto());
            limparCampus();
            habilitarCampus(3);
        }
    }//GEN-LAST:event_JBexcluirActionPerformed

    private void JBalterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBalterarActionPerformed

        int opcao = JOptionPane.showConfirmDialog(null, "Deseja realmente atualizar os dados da cidade", "Mensagem do Sistema", JOptionPane.INFORMATION_MESSAGE);
        if (opcao == 0) {
            controller.alterar(this.criarObjeto());
            
            limparCampus();
            habilitarCampus(3);
        }
        if(this.getOpcao() == 1){
            TelaTabelaCidadeInternalFrame tabelaCidade = new TelaTabelaCidadeInternalFrame();
            tabelaCidade.setVisible(true);
            this.getAreaTrabalho().add(tabelaCidade);
            
            this.dispose();
        }
    }//GEN-LAST:event_JBalterarActionPerformed

    private void JBsalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBsalvarActionPerformed
        if (this.verificarCampus()) {
            controller.cadastrar(this.criarObjeto());
            limparCampus();
            habilitarCampus(3);
        }
        if(this.getOpcao() == 1){
            TelaTabelaCidadeInternalFrame tabelaCidade = new TelaTabelaCidadeInternalFrame();
            tabelaCidade.setVisible(true);
            this.getAreaTrabalho().add(tabelaCidade);
            
            this.dispose();
        }
    }//GEN-LAST:event_JBsalvarActionPerformed

